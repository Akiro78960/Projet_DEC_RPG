class Equipment{
    constructor(name, type, element, range, bonusHP, bonusMP, bonusAtk, bonusDef, bonusAtkM, bonusDefM, bonusSpeed, bonusCrit, vulneFire, vulneThunder, vulneIce){
        this.name = name
        this.type = type
        this.element = element
        this.range = range
        this.bonusHP = bonusHP
        this.bonusMP = bonusMP
        this.bonusAtk = bonusAtk
        this.bonusDef = bonusDef
        this.bonusAtkM = bonusAtkM
        this.bonusDefM = bonusDefM
        this.bonusSpeed = bonusSpeed
        this.bonusCrit = bonusCrit
        this.vulneFire = vulneFire
        this.vulneThunder = vulneThunder
        this.vulneIce = vulneIce
    }
}
